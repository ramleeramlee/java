package 확인문제;

public class Mask {
	
	String color;
	int money;
	int count;
	
	public Mask(String color, int money, int count) {
		super();
		this.color = color;
		this.money = money;
		this.count = count;
	}


	@Override
	public String toString() {
		return "Mask [color=" + color + ", money=" + money + ", count=" + count + "]";
	}
	
	
	
	
	
	

}
